RenderableType = object  # duck-typed; anything Rich can render


def render_sprite() -> str:
    """Return the sprite as Rich markup."""
    return _SPRITE_MARKUP


def get_welcome() -> RenderableType:
    """Full static welcome panel (sprite + figlet wordmark + hints)."""
    return _build_welcome()


def _build_welcome(shimmer_col: int | None = None) -> RenderableType:
    """Build the side-by-side welcome panel.

    Left column : chafa sprite
    Right column: figlet wordmark (with optional shimmer), badge,
                  tagline, and version.
    Below       : keyboard-shortcut hints.
    """
    num_rows = len(_SPRITE_LINES)

    wordmark = _render_wordmark("#0078D4", shimmer_col)

    # Right-column content, vertically centred against the sprite.
    right: list[str] = [""] * num_rows
    start = max(0, (num_rows - 15) // 2)
    for i, ln in enumerate(wordmark):
        right[start + i] = ln
    # 1-row gap, then badge
    right[start + 7] = "[bold #FFB900]UNOFFICIAL TUI[/]"
    # 1-row gap, then metadata
    right[start + 9] = "[dim]Community-built terminal UI[/]"
    right[start + 10] = "[dim]for Microsoft Fabric OneLake[/]"
    right[start + 11] = "[dim]Not affiliated with Microsoft[/]"
    right[start + 12] = f"[dim]v{_APP_VERSION}[/]"

    tbl = Table(
        show_header=False,
        show_edge=False,
        show_lines=False,
        pad_edge=False,
        box=None,
        padding=(0, 3, 0, 0),
    )
    tbl.add_column(no_wrap=True)
    tbl.add_column(no_wrap=True)
    for i in range(num_rows):
        tbl.add_row(_SPRITE_LINES[i], right[i])

    hints = Text.from_markup(
        "\n  [dim]↑↓ or j/k Navigate  │  Enter Expand  │  / Search[/]"
        "\n  [dim]Tab/h/l Panels  │  g/G Top/Bottom  │  y Copy menu  │  ? Help[/]\n"
    )

    return Group(tbl, hints)


class OneLakeSprite(Static):
    """Unofficial OneLake-inspired splash art with shimmer animation."""

    DEFAULT_CSS = """
    OneLakeSprite {
        width: auto;
        height: auto;
    }
    """

    _START = -_SHIMMER_BAND - 2
    _END = _WORDMARK_WIDTH + 2

    def __init__(self, **kwargs) -> None:
        super().__init__(_build_welcome(), **kwargs)
        self._frame = self._START

    def on_mount(self) -> None:
        total = self._END - self._START
        self.set_interval(0.06, self._tick, repeat=total)

    def _tick(self) -> None:
        self._frame += 1
        col = self._frame if 0 <= self._frame < _WORDMARK_WIDTH else None
        self.update(_build_welcome(shimmer_col=col))
