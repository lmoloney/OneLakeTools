# ── Convert ANSI escape sequences to Rich markup once at import ──────────

_SGR_RE = re.compile(r"\x1b\[([0-9;]+)m")


def _ansi_line_to_rich(line: str) -> str:
    """Convert a single line of ANSI SGR sequences to Rich markup.

    ``re.split`` with a capturing group alternates between visible text
    (even indices) and captured SGR parameter strings (odd indices).
    We track position to avoid misclassifying visible chars like ``4``
    as SGR codes.
    """
    parts: list[str] = []
    color_open = False
    for i, segment in enumerate(_SGR_RE.split(line)):
        if not segment:
            continue
        if i % 2 == 0:
            # Visible text between ANSI sequences
            parts.append(segment)
        else:
            # Captured SGR parameter string
            if segment == "39":
                if color_open:
                    parts.append("[/]")
                    color_open = False
            elif segment.startswith("38;2;"):
                if color_open:
                    parts.append("[/]")
                _, _, r, g, b = segment.split(";")
                parts.append(f"[rgb({r},{g},{b})]")
                color_open = True
    if color_open:
        parts.append("[/]")
    return "".join(parts)


