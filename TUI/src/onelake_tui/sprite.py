_WORDMARK_WIDTH = max(len(ln) for ln in _WORDMARK_LINES)
_SHIMMER_COLOR = "#E8F4FD"
_SHIMMER_BAND = 8


def _render_wordmark(color: str, shimmer_col: int | None = None) -> list[str]:
    """Render the figlet wordmark as Rich-markup lines with optional shimmer."""
    if shimmer_col is None:
        return [f"[bold {color}]{ln}[/]" for ln in _WORDMARK_LINES]

    result: list[str] = []
    for ln in _WORDMARK_LINES:
        parts: list[str] = []
        run_color: str | None = None
        run_chars: list[str] = []
        for i, ch in enumerate(ln):
            if ch == " ":
                if run_chars:
                    parts.append(f"[bold {run_color}]{''.join(run_chars)}[/]")
                    run_chars.clear()
                    run_color = None
                parts.append(" ")
            else:
                c = _SHIMMER_COLOR if shimmer_col <= i < shimmer_col + _SHIMMER_BAND else color
                if c == run_color:
                    run_chars.append(ch)
                else:
                    if run_chars:
                        parts.append(f"[bold {run_color}]{''.join(run_chars)}[/]")
                    run_color = c
                    run_chars = [ch]
        if run_chars:
            parts.append(f"[bold {run_color}]{''.join(run_chars)}[/]")
        result.append("".join(parts))
    return result


